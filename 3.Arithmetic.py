# Arithmetic = [+] (Addition) 
#              [-] (Substraction)
#              [*] (multiplication)
#              [/] (Division)
#              [//] (integer division)
#              [%] (remainder)


number = 5

number = number + 5  # number += 5
print(number)

number = number - 5 # number -= 5
print(number)

number = number * 5 # number *= 5
print(number)

number = number / 5 # number /+ 5
print(number)

number = number // 5 # number //= 5
print(number)

number = number % 5 # number %= 5
print(number)

print("End!!")





